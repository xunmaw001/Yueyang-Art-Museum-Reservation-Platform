            yonghuOptions: [],//用户
            yonghuIndex : 0,//用户下标

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let meishuguanParams = {
                page: 1,
                limit: 100,
            }
            let meishuguanData = await this.$api.page(`meishuguan`, meishuguanParams);
            this.meishuguanOptions = meishuguanData.data.list;
            let yonghuParams = {
                page: 1,
                limit: 100,
            }
            let yonghuData = await this.$api.page(`yonghu`, yonghuParams);
            this.yonghuOptions = yonghuData.data.list;
		/*下拉框*/
			let meishuguanOrderTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'meishuguan_order_types',
            }
			let meishuguanOrderTypes = await this.$api.page(`dictionary`, meishuguanOrderTypesParams);
			this.meishuguanOrderTypesOptions = meishuguanOrderTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`meishuguanOrder`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.meishuguanOrderId){
                this.ruleForm.meishuguanOrderId = options.meishuguanOrderId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            meishuguanChange(e) {
                this.meishuguanIndex = e.target.value
                this.ruleForm.meishuguanName = this.meishuguanOptions[this.meishuguanIndex].meishuguanName
                this.ruleForm.meishuguanId = this.meishuguanOptions[this.meishuguanIndex].id
            },
            yonghuChange(e) {
                this.yonghuIndex = e.target.value
                this.ruleForm.yonghuName = this.yonghuOptions[this.yonghuIndex].yonghuName
                this.ruleForm.yonghuId = this.yonghuOptions[this.yonghuIndex].id
            },
            // 下拉变化
            meishuguanOrderTypesChange(e) {
                this.meishuguanOrderTypesIndex = e.target.value
                this.ruleForm.meishuguanOrderValue = this.meishuguanOrderTypesOptions[this.meishuguanOrderTypesIndex].indexName
                this.ruleForm.meishuguanOrderTypes = this.meishuguanOrderTypesOptions[this.meishuguanOrderTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.meishuguanOrderUuidNumber)) {
                    this.$utils.msg(`订单编号不能为空`);
                    return
                }
				if ((!this.ruleForm.buyNumber) && (!this.$validate.isIntNumer(this.ruleForm.buyNumber)) && this.ruleForm.buyNumber >0) {
                    this.$utils.msg(`预约人数不能为空，不能小于0 格式为数字`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`meishuguanOrder`, this.ruleForm);
                } else {
                    await this.$api.save(`meishuguanOrder`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
